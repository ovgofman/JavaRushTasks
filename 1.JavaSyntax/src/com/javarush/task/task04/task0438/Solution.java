package com.javarush.task.task04.task0438;

/* 
Рисуем линии
*/

public class Solution {
    public static void main(String[] args) throws Exception {
        int digit = 8;
        for (int x = 0; x < 10; x++) {
            System.out.print(digit);
        }
        System.out.println();
        for (int y = 0; y < 10; y++) {
            System.out.println(digit);
        }
    }
}
